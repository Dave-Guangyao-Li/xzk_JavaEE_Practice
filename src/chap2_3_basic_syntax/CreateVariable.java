package chap2_3_basic_syntax;

public class CreateVariable {
	/**
	 * 创建变量
	 * @param args
	 */
	public static void main(String[] args) {
		// 创建变量:数据类型 变量名=初始化值；
		int a = 20;
		//创建变量暂不赋值：数据类型 变量名；
		int b;
		//创建字符串变量
		String s = "abcde";
		a = 30;
		System.out.println(a); //syso+alt+?可以自动联想
	}

}
